def render_pause_resume_cancel(cfg):
    """PAUSE, RESUME, CANCEL_PRINT"""
    output = []

    # PAUSE
    output.append("[gcode_macro PAUSE]")
    output.append("description: Pause the running print")
    output.append("rename_existing: PAUSE_BASE")
    output.append("variable_extrude: 5.0")
    output.append("gcode:")
    output.append("  SAVE_VARIABLE VARIABLE=fan_speed VALUE={printer.fan.speed * 255}")
    output.append("  {% set act_x = printer.toolhead.position.x|float %}")
    output.append("  {% set act_y = printer.toolhead.position.y|float %}")
    output.append("  {% set act_z = printer.toolhead.position.z|float %}")
    output.append("  SAVE_VARIABLE VARIABLE=pause_x VALUE={act_x}")
    output.append("  SAVE_VARIABLE VARIABLE=pause_y VALUE={act_y}")
    output.append("  SAVE_VARIABLE VARIABLE=pause_z VALUE={act_z}")
    output.append("  M106 S0")
    output.append("  {% set E = printer[\"gcode_macro PAUSE\"].extrude|float %}")
    output.append("  {% set x_park = printer.toolhead.axis_maximum.x|float %}")
    output.append("  {% set y_park = printer.toolhead.axis_maximum.y|float / 2 %}")
    output.append("  {% set max_z = printer.toolhead.axis_maximum.z|float %}")
    output.append("  {% set act_z = printer.toolhead.position.z|float %}")
    output.append("  {% if act_z < (max_z - 2.0) %}")
    output.append("    {% set z_safe = 2.0 %}")
    output.append("  {% else %}")
    output.append("    {% set z_safe = max_z - act_z %}")
    output.append("  {% endif %}")
    output.append("  PAUSE_BASE")
    output.append("  G91")
    output.append("  {% if printer.extruder.can_extrude|lower == 'true' %}")
    output.append("    G1 E-{E} F2100")
    output.append("  {% else %}")
    output.append("    {action_respond_info(\"Extruder not hot enough\")}")
    output.append("  {% endif %}")
    output.append("  {% if \"xyz\" in printer.toolhead.homed_axes %}")
    output.append("    G1 Z{z_safe} F900")
    output.append("    G90")
    output.append("    G1 X{x_park} Y{y_park} F6000")
    output.append("  {% else %}")
    output.append("    {action_respond_info(\"Printer not homed\")}")
    output.append("  {% endif %}")
    output.append("  M117 Pause")
    output.append("")

    # RESUME
    output.append("[gcode_macro RESUME]")
    output.append("description: Resume the running print")
    output.append("rename_existing: RESUME_BASE")
    output.append("variable_prime: 40.0")
    output.append("gcode:")
    output.append("  {% set svv = printer.save_variables.variables %}")
    output.append("  {% set fan_speed = svv.fan_speed|default(0)|int %}")
    output.append("  {% set x_park = printer.toolhead.axis_maximum.x|float %}")
    output.append("  G1 X{x_park} F6000")
    output.append("  {% if 'pause_x' in svv and 'pause_y' in svv and 'pause_z' in svv %}")
    output.append("    {% set px = svv.pause_x|float %}")
    output.append("    {% set py = svv.pause_y|float %}")
    output.append("    {% set pz = svv.pause_z|float %}")
    output.append("  {% else %}")
    output.append("    {action_respond_info(\"Pause coordinates not found.\")}")
    output.append("    M117 ERROR: Pause position unknown.")
    output.append("  {% endif %}")
    output.append("  {% set E = printer[\"gcode_macro PAUSE\"].extrude|float %}")
    output.append("  {% if 'VELOCITY' in params|upper %}")
    output.append("    {% set get_params = ('VELOCITY=' + params.VELOCITY) %}")
    output.append("  {% else %}")
    output.append("    {% set get_params = \"\" %}")
    output.append("  {% endif %}")
    output.append("  {% if printer.extruder.can_extrude|lower == 'true' %}")
    output.append("    G91")
    output.append("    G1 E{prime|float} F300")
    output.append("    G92 E0")
    output.append("    M106 S255")
    output.append("    G4 P5000")
    output.append("    G90")
    output.append("    G1 X{x_park - 11} F26000")
    output.append("    G92 E0")
    output.append("    G1 X{px} Y{py} Z{pz} F6000")
    output.append("    G1 E{E} F10800")
    output.append("    M106 S{fan_speed}")
    output.append("  {% else %}")
    output.append("    {action_respond_info(\"Extruder not hot enough\")}")
    output.append("  {% endif %}")
    output.append("  RESUME_BASE {get_params}")
    output.append("  M117 Printing...")
    output.append("")

    # CANCEL_PRINT
    output.append("[gcode_macro CANCEL_PRINT]")
    output.append("rename_existing: CANCEL_PRINT_DEFAULT")
    output.append("description: Cancel print, retract and park head safely")
    output.append("gcode:")
    output.append("  M220 S100")
    output.append("  M221 S100")
    output.append("  SAVE_VARIABLE VARIABLE=was_interrupted VALUE=False")
    output.append("  SAVE_VARIABLE VARIABLE=printing_start VALUE=False")
    output.append("  {% set retract_mm = 20.0 %}")
    output.append("  {% set z_lift = 10.0 %}")
    output.append("  {% set x_park = printer.configfile.settings['stepper_x']['position_max']|float %}")
    output.append("  {% set y_park = printer.configfile.settings['stepper_y']['position_max']|float %}")
    output.append("  {% set z_max = printer.toolhead.axis_maximum.z|float %}")
    output.append("  {% set minT = printer.configfile.settings['extruder']['min_extrude_temp']|float %}")
    output.append("  SET_VELOCITY_LIMIT ACCEL=20000 ACCEL_TO_DECEL=10000")
    output.append("  {% if printer.extruder.temperature >= minT %}")
    output.append("    G1 E-{retract_mm} F11000")
    output.append("    G92 E0")
    output.append("  {% endif %}")
    output.append("  G91")
    output.append("  {% set dz = z_lift if (printer.gcode_move.position.z + z_lift) < z_max else (z_max - printer.gcode_move.position.z) %}")
    output.append("  G1 Z+{dz} F3000")
    output.append("  G90")
    output.append("  G1 X{x_park} F9000")
    output.append("  G1 Y{y_park} F3000")
    output.append("  G92 E0")
    output.append("  G1 E-15 F100")
    output.append("  G92 E0")
    output.append("  G1 E-10 F100")
    output.append("  G92 E0")
    output.append("  G1 E-85 F4000")
    output.append("  ALL_FAN_OFF")
    output.append("  TURN_OFF_HEATERS")
    output.append("  M84")
    output.append("  {action_respond_info(\"Cancel Print executed\")}")
    output.append("  CANCEL_PRINT_DEFAULT")
    output.append("")

    return "\n".join(output)


