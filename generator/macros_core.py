def render_gcode_offset(cfg):
    """SET_GCODE_OFFSET persistent"""
    output = []

    output.append("[gcode_macro SET_GCODE_OFFSET]")
    output.append("rename_existing: _SET_GCODE_OFFSET")
    output.append("gcode:")
    output.append("  {% if printer.save_variables.variables.gcode_offsets %}")
    output.append("    {% set offsets = printer.save_variables.variables.gcode_offsets %}")
    output.append("  {% else %}")
    output.append("    {% set offsets = {'x': None,'y': None,'z': None} %}")
    output.append("  {% endif %}")
    output.append("  {% set ns = namespace(offsets={'x': offsets.x,'y': offsets.y,'z': offsets.z}) %}")
    output.append("  _SET_GCODE_OFFSET {% for p in params %}{'%s=%s '% (p, params[p])}{% endfor %}")
    output.append("  {%if 'X' in params %}{% set null = ns.offsets.update({'x': params.X}) %}{% endif %}")
    output.append("  {%if 'Y' in params %}{% set null = ns.offsets.update({'y': params.Y}) %}{% endif %}")
    output.append("  {%if 'Z' in params %}{% set null = ns.offsets.update({'z': params.Z}) %}{% endif %}")
    output.append("  {%if 'Z_ADJUST' in params %}")
    output.append("    {%if ns.offsets.z == None %}{% set null = ns.offsets.update({'z': 0}) %}{% endif %}")
    output.append("    {% set null = ns.offsets.update({'z': (ns.offsets.z | float) + (params.Z_ADJUST | float)}) %}")
    output.append("  {% endif %}")
    output.append("  SAVE_VARIABLE VARIABLE=gcode_offsets VALUE=\"{ns.offsets}\"")
    output.append("")

    return "\n".join(output)


