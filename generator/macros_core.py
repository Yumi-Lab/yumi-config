def render_marlin_compat(cfg):
    """M201, M203, M205, M900, M106/M107 overrides"""
    output = []

    # M106 override (Aux_Fan sur P2, Motherboard sur P3)
    output.append("[gcode_macro M106]")
    output.append("rename_existing: G106")
    output.append("gcode:")
    output.append("  {% if params.P is defined %}")
    output.append("    {% if params.P|int == 2 %}")
    output.append("      {% if params.S is defined %}")
    output.append("        {% if params.S|int == 255 %}")
    output.append("          {% set realspeed = 1 %}")
    output.append("        {% elif params.S|int == 0 %}")
    output.append("          {% set realspeed = 0 %}")
    output.append("        {% else %}")
    output.append("          {% set realspeed = params.S|float/255 %}")
    output.append("        {% endif %}")
    output.append("      {% else %}")
    output.append("        {% set realspeed = 1 %}")
    output.append("      {% endif %}")
    output.append("      SET_FAN_SPEED FAN=Aux_Fan SPEED={realspeed}")
    output.append("    {% endif %}")
    output.append("  {% else %}")
    output.append("    {% if params.S is defined %}")
    output.append("      G106 S{params.S}")
    output.append("    {% else %}")
    output.append("      G106 S255")
    output.append("    {% endif %}")
    output.append("  {% endif %}")
    output.append("")

    # M107 override
    output.append("[gcode_macro M107]")
    output.append("rename_existing: G107")
    output.append("gcode:")
    output.append("  {% if params.P is defined %}")
    output.append("    {% if params.P|int == 2 %}")
    output.append("      SET_FAN_SPEED FAN=Aux_Fan SPEED=0")
    output.append("    {% endif %}")
    output.append("  {% else %}")
    output.append("    SET_FAN_SPEED FAN=Aux_Fan SPEED=0")
    output.append("    G107")
    output.append("  {% endif %}")
    output.append("")

    # M201
    output.append("[gcode_macro M201]")
    output.append("description: Sets maximum acceleration.")
    output.append("variable_max_accel: 1.7976931348623157e+308")
    output.append("gcode:")
    output.append("  {% if 'X' in params or 'Y' in params %}")
    output.append("    {% set accel = (params.X|default(params.Y)|float, params.Y|default(params.X)|float)|min %}")
    output.append("    SET_GCODE_VARIABLE MACRO=M201 VARIABLE=max_accel VALUE=\"{accel}\"")
    output.append("  {% else %}")
    output.append("    SET_VELOCITY_LIMIT")
    output.append("  {% endif %}")
    output.append("")

    # M203
    output.append("[gcode_macro M203]")
    output.append("description: Sets maximum velocity.")
    output.append("gcode:")
    output.append("  {% if 'X' in params or 'Y' in params %}")
    output.append("    {% set velocity = (params.X|default(params.Y)|float, params.Y|default(params.X)|float)|min %}")
    output.append("    SET_VELOCITY_LIMIT VELOCITY=\"{velocity}\"")
    output.append("  {% else %}")
    output.append("    SET_VELOCITY_LIMIT")
    output.append("  {% endif %}")
    output.append("")

    # M205
    output.append("[gcode_macro M205]")
    output.append("description: Sets square corner velocity.")
    output.append("gcode:")
    output.append("  {% if 'X' in params or 'Y' in params %}")
    output.append("    SET_VELOCITY_LIMIT SQUARE_CORNER_VELOCITY=\"{(params.X|default(0)|float, params.Y|default(0)|float)|min}\"")
    output.append("  {% else %}")
    output.append("    SET_VELOCITY_LIMIT")
    output.append("  {% endif %}")
    output.append("")

    # M900
    output.append("[gcode_macro M900]")
    output.append("description: Set pressure advance (Marlin compat)")
    output.append("gcode:")
    output.append("  {% if 'K' in params %}")
    output.append("    {% if 'E' in params %}")
    output.append("      SET_PRESSURE_ADVANCE EXTRUDER={params.E} ADVANCE={params.K}")
    output.append("    {% else %}")
    output.append("      SET_PRESSURE_ADVANCE ADVANCE={params.K}")
    output.append("    {% endif %}")
    output.append("  {% endif %}")
    output.append("")

    return "\n".join(output)
